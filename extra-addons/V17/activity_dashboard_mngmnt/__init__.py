from . import models

def post_init_hook(env):
    rec = env.ref("mail.mail_activity_rule_user")
    rec.write({'perm_read': True, 'perm_create': True})

def uninstall_hook(env):
    rec = env.ref("mail.mail_activity_rule_user")
    rec.write({'perm_read': False, 'perm_create': False})

